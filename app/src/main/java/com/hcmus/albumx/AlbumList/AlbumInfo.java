package com.hcmus.albumx.AlbumList;

public class AlbumInfo {
    public int id;
    public String name;
    public int type;
    public int icon;

    public AlbumInfo(int id, String name, int type, int icon){
        this.id = id;
        this.name=name;
        this.type=type;
        this.icon=icon;
    }
}
